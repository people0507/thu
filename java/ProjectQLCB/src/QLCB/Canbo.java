package QLCB;

public class Canbo {
    private int SoTK;
    private String HoTen;
    private String GT;
    private String Diachi;
    private int Luong;

    public String getHoTen() {
        return HoTen;
    }

    public void setHoTen(String hoTen) {
        HoTen = hoTen;
    }

    public String getGT() {
        return GT;
    }

    public void setGT(String GT) {
        this.GT = GT;
    }

    public String getDiachi() {
        return Diachi;
    }

    public void setDiachi(String diachi) {
        Diachi = diachi;
    }

    public int getLuong() {
        return Luong;
    }

    public void setLuong(int luong) {
        Luong = luong;
    }

    public int getSoTK() {
        return SoTK;
    }

    public void setSoTK(int soTK) {
        SoTK = soTK;
    }
}
